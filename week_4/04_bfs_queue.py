# 위의 그래프를 예시로 삼아서 인접 리스트 방식으로 표현했습니다!
graph = {
    1: [2, 5, 9],
    2: [1, 3],
    3: [2, 4],
    4: [3],
    5: [1, 6, 8],
    6: [5, 7],
    7: [6],
    8: [5],
    9: [1, 10],
    10: [9]
}

def bfs_queue(adj_graph, start_node):
    queue = [1]
    visited = []

    while queue:
        visited_node = queue.pop(0)
        visited.append(visited_node)
        for node in adj_graph[visited_node]:
            if node not in visited:
                queue.append(node)

    return visited


print(bfs_queue(graph, 1))  # 1 이 시작노드입니다!
